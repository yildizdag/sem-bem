function [N, dN] = linear2Dshapefun(xi,eta)

N = zeros(size(xi,1),4);
N(:,1) = 0.25.*(1-xi).*(1-eta);
N(:,2) = 0.25.*(1+xi).*(1-eta);
N(:,3) = 0.25.*(1+xi).*(1+eta);
N(:,4) = 0.25.*(1-xi).*(1+eta);

dN = zeros(2,4,size(xi,1));
dN(1,1,:) = -0.25.*(1-eta);
dN(1,2,:) =  0.25.*(1-eta);
dN(1,3,:) =  0.25.*(1+eta);
dN(1,4,:) = -0.25.*(1+eta);
dN(2,1,:) = -0.25.*(1-xi);
dN(2,2,:) = -0.25.*(1+xi);
dN(2,3,:) =  0.25.*(1+xi);
dN(2,4,:) =  0.25.*(1-xi);